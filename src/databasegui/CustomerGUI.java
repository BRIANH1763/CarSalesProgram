    private void queryButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_queryButtonActionPerformed
        String name = txtSearch.getText();
        //query database for students with LastName = name         
        arrayOfCustomers = CustomerConnection.getCustomersByName(name);
        numberOfRecords = CustomerConnection.numRecordsInQuery;
        //if there were any matches  
        currentIndex = 0;
        if (numberOfRecords != 0) {
            //display the first students details             
            String idText = Integer.toString(arrayOfCustomers[currentIndex].getcustomerID());
            customerIDTextField.setText(idText);
            firstnameTextField.setText(arrayOfCustomers[currentIndex].getFirstname());
            surnameTextField.setText(arrayOfCustomers[currentIndex].getSurname());
            streetTextField.setText(arrayOfCustomers[currentIndex].getStreet());
            townTextField.setText(arrayOfCustomers[currentIndex].getTown());
            cityTextField.setText(arrayOfCustomers[currentIndex].getCity());
            postcodeTextField.setText(arrayOfCustomers[currentIndex].getPostcode());

            //display the current index for current and last record             
            currRecordTextField.setText(Integer.toString(currentIndex + 1));
            maxRecordTextField.setText(Integer.toString(numberOfRecords));
        } else {
            JOptionPane.showMessageDialog(this, "No Records found", "Query Result", JOptionPane.PLAIN_MESSAGE);
        }             // TODO add your handling code here:
    }//GEN-LAST:event_queryButtonActionPerformed

