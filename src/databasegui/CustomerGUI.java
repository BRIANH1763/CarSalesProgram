    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        boolean done = CustomerConnection.addCustomer(firstnameTextField.getText(), surnameTextField.getText(), streetTextField.getText(), townTextField.getText(), cityTextField.getText(), postcodeTextField.getText(), contactNumberTextField.getText());
        if (done) {
            JOptionPane.showMessageDialog(this, "Customer added", "Adding Record", JOptionPane.PLAIN_MESSAGE);
            //reload the records by calling the browseButtonActionPerformed method with the event  
            browseButtonActionPerformed(evt);
        } else {
            JOptionPane.showMessageDialog(this, "Customer not added", "Error", JOptionPane.PLAIN_MESSAGE);
        }        // TODO add your handling code here:
    }//GEN-LAST:event_addButtonActionPerformed

