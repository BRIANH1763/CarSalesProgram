    private void btnNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNextActionPerformed
        numberOfRecords = CustomerConnection.numRecordsInQuery;
        //button will move to the record after current one         
        //if the last record is showing there will be no response         
        if (currentIndex < numberOfRecords - 1) {
            currentIndex++;
            currRecordTextField.setText(Integer.toString(currentIndex + 1));
            //display the students details at currentIndex             
            String idText = Integer.toString(arrayOfCustomers[currentIndex].getcustomerID());
            customerIDTextField.setText(idText);
            firstnameTextField.setText(arrayOfCustomers[currentIndex].getFirstname());
            surnameTextField.setText(arrayOfCustomers[currentIndex].getSurname());
            streetTextField.setText(arrayOfCustomers[currentIndex].getStreet());
            townTextField.setText(arrayOfCustomers[currentIndex].getTown());
            cityTextField.setText(arrayOfCustomers[currentIndex].getCity());
            postcodeTextField.setText(arrayOfCustomers[currentIndex].getPostcode());
            contactNumberTextField.setText(arrayOfCustomers[currentIndex].getContactNumber());

        }         // TODO add your handling code here:
    }//GEN-LAST:event_btnNextActionPerformed

