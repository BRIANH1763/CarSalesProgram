    private void btnListActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListActionPerformed
        //adding all of the cars from the database into the list box 
        lstCars.removeAll();
        data = dc.getAllCars();
        Vector listData = new Vector(); 
        for(int index=0;index<data.size();index++){
            CarsClass temp = (CarsClass)data.get(index);
            listData.addElement(temp.getCarID()+"  "+temp.getMake()+"  "+temp.getModel()+"  "+temp.getColour()+"  "+temp.getYear()+"  "+temp.getEngineSize()+"  "+temp.getBodyType()+"  "+temp.getMileage()+"  "+temp.getTransmission()+"  "+temp.getFuelType()+"  "+temp.getCondition()+"  "+temp.getPrice());
        }
        lstCars.setListData(listData);
    }//GEN-LAST:event_btnListActionPerformed

