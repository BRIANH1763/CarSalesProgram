    private void lstCarsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lstCarsMouseClicked
        // TODO add your handling code herint index = lstCars.getSelectedIndex();
        //when selecting a car from the list box, this method allows for the information
        //to be copied into the above fields along with the picture of the car
        int index = lstCars.getSelectedIndex();
        if (data != null && index != -1){
            CarsClass temp = (CarsClass)data.get(index);
            txtCarID.setText(temp.getCarID());
            txtCarMake.setText(temp.getMake());
            txtCarModel.setText(temp.getModel());
            txtCarColour.setText(temp.getColour());
            txtCarYear.setText(temp.getYear());
            txtCarEngine.setText(temp.getEngineSize());
            txtBodyType.setText(temp.getBodyType());
            txtMileage.setText(temp.getMileage());
            txtTransmission.setText(temp.getTransmission());
            txtFuelType.setText(temp.getFuelType());
            txtCondition.setText(temp.getCondition());
            txtPrice.setText(temp.getPrice());   
            Image pic = temp.getImage();
            int w = lblPicture.getWidth();
            int h = lblPicture.getHeight();
            pic = pic.getScaledInstance(w,h,1);
            lblPicture.setIcon(new ImageIcon(pic));
            
    }//GEN-LAST:event_lstCarsMouseClicked
    }
